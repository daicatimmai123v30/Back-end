package Model;

public class GioHang {
	private int IDCart;
	private int Amount;
	private int Price;
	private int Total;
	private int IDOder;
	public int getIDCart() {
		return IDCart;
	}
	public void setIDCart(int iDCart) {
		IDCart = iDCart;
	}
	public int getAmount() {
		return Amount;
	}
	public void setAmount(int amount) {
		Amount = amount;
	}
	public int getPrice() {
		return Price;
	}
	public void setPrice(int price) {
		Price = price;
	}
	public int getTotal() {
		return Total;
	}
	public void setTotal(int total) {
		Total = total;
	}
	public int getIDOder() {
		return IDOder;
	}
	public void setIDOder(int iDOder) {
		IDOder = iDOder;
	}
	
}
